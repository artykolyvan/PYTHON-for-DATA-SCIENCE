# Дополнительные возможности "print"

name = "Tom"
age = 50
print(name, age)
print(name, age, sep=' / ')     # применяем сепаратор с любым разделителем
print(name, age, sep=' * ')     # применяем сепаратор с любым разделителем
print(name, age, sep='+++')     # применяем сепаратор с любым разделителем
print('*' *20)
print(name, end=' + ')     # по умолчанию end='\n', но можно поставить любой символ или комбинацию.
print(age, end=' - ')
print('end')
print('*' *20)
print(name, end=': ')
print(age, end=' - ')
print('end', end=' /pyp/')




